v1=float(input("Введите скорость первого автомобиля: "))
v2=float(input("Введите скорость второго автомобиля: "))
S=float(input("Введите расстояние между автомобилями: "))
T=float(input("Введите время пути: "))
ST=S+v1*T+v2*T
print("Чеоез", T, "часов расстояние между автомобилями составляет", ST, "км")
